-- Create tables
CREATE TABLE customer(
    customer_id SERIAL PRIMARY KEY,
    forename    VARCHAR(20) NOT NULL,
    surname     VARCHAR(20) NOT NULL,
    email       VARCHAR(254),
    phone       VARCHAR(20),
    address1    VARCHAR(40) NOT NULL,
    address2    VARCHAR(40),
    town        VARCHAR(60),
    postcode    CHAR(9) NOT NULL,
    CONSTRAINT contact_method CHECK (email IS NOT NULL OR phone IS NOT NULL) -- Make sure every customer has at least one contact method
);

CREATE TABLE boat(
    boat_id         SERIAL PRIMARY KEY,
    owner_id        INT NOT NULL REFERENCES customer(customer_id),
    builder         VARCHAR(30) NOT NULL,
    model           VARCHAR(30),
    name            VARCHAR(50) NOT NULL,
    engine_size     INT,
    engine_count    INT,
    fuel_type       VARCHAR(10),
    build_date      DATE
);

CREATE TABLE service_category(
    category_id     SERIAL PRIMARY KEY,
    category_title  VARCHAR(25)
);

CREATE TABLE service(
    service_id      SERIAL PRIMARY KEY,
    category_id     INT NOT NULL,
    description     VARCHAR(200),
    scheduled_datetime  TIMESTAMP NOT NULL,
    completed_datetime  TIMESTAMP,
    boat_id         INT NOT NULL REFERENCES boat(boat_id)
);

CREATE TABLE boatyard(
    boatyard_id SERIAL PRIMARY KEY,
    phone       VARCHAR(20) NOT NULL,
    address1    VARCHAR(40) NOT NULL,
    address2    VARCHAR(40),
    town        VARCHAR(60),
    postcode    CHAR(9) NOT NULL
);

CREATE TABLE staff(
    staff_id    SERIAL PRIMARY KEY,
    forename    VARCHAR(20) NOT NULL,
    surname     VARCHAR(20) NOT NULL,
    work_email  VARCHAR(254) NOT NULL,
    work_phone  VARCHAR(20),
    home_email  VARCHAR(254),
    home_phone  VARCHAR(20),
    address1    VARCHAR(40) NOT NULL,
    address2    VARCHAR(40),
    town        VARCHAR(60),
    postcode    CHAR(9) NOT NULL,
    boatyard_id INT NOT NULL REFERENCES boatyard(boatyard_id),
    CONSTRAINT contact_method CHECK (home_email IS NOT NULL OR home_phone IS NOT NULL) -- Make sure every member of staff has at least one home contact method
);

CREATE TABLE staff_service(
    staff_id    INT NOT NULL REFERENCES staff(staff_id),
    service_id  INT NOT NULL REFERENCES service(service_id),
    work_hours  DECIMAL(4,1),

    PRIMARY KEY (staff_id, service_id)
);

CREATE TABLE role(
    role_id     SERIAL PRIMARY KEY,
    role_title  VARCHAR(30)
);

CREATE TABLE staff_role(
    role_id     INT NOT NULL REFERENCES role(role_id),
    staff_id    INT NOT NULL REFERENCES staff(staff_id),

    PRIMARY KEY (role_id, staff_id)
);

